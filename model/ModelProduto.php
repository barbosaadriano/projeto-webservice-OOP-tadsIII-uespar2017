 *
 * @author Administrador
 */
class ModelProduto {

    private $id;
    private $nome;

    function getId() {
        return $this->id;
    }

    function getNome() {
        return $this->nome;
    }

    function setId($id) {
        $this->id = $id;
    }

    function setNome($nome) {
        $this->nome = $nome;
    }

    function __construct($id, $nome=null) {
        $this->id = $id;
        $this->nome = $nome;
    }

}
